package java__lab2;
//14.	Write a program that prints the value from 1 to 10 skipping 4.
public class Skip {
    public static void main(String[] args){
        for (int i = 1; i <= 10; i++) {
            if (i!=4) {
            System.out.println(i+ " is not 4");
            }
        }
    }
}
